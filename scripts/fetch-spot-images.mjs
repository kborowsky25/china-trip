// Fetches a Wikipedia image for every "see"-spot title and writes
// mobile/lib/spotImages.ts so each attraction shows a real photo.
// Run: node scripts/fetch-spot-images.mjs
import fs from "node:fs";
import path from "node:path";

const TITLES = [
  "Chengdu Research Base of Giant Panda Breeding", "Ciqikou", "Forbidden City",
  "Hongya Cave", "Jinli", "Jiufen", "Leshan Giant Buddha", "Liziba station",
  "Mutianyu", "National Palace Museum", "Oriental Pearl Tower", "Ruins of St. Paul's",
  "Shilin Night Market", "Star Ferry", "Summer Palace", "Taipei 101",
  "Temple of Heaven", "The Bund", "Tian Tan Buddha", "Tianmen Mountain",
  "Victoria Peak", "Wuhou Shrine", "Wulingyuan", "Yangtze River Cableway",
  "Yu Garden", "Zhangjiajie Glass Bridge", "Zhangjiajie National Forest Park", "Zhujiajiao",
];

// Manual overrides where the Wikipedia page image is a logo/map/poor photo.
const OVERRIDE = {
  "Taipei 101": "https://upload.wikimedia.org/wikipedia/commons/thumb/2/2d/Taipei_Taiwan_Taipei-101-Tower-01.jpg/960px-Taipei_Taiwan_Taipei-101-Tower-01.jpg",
  "Star Ferry": "https://upload.wikimedia.org/wikipedia/commons/thumb/4/4f/Star_Ferry%2C_Victoria_Harbour_%2831002395685%29.jpg/960px-Star_Ferry%2C_Victoria_Harbour_%2831002395685%29.jpg",
  Ciqikou: "https://upload.wikimedia.org/wikipedia/commons/1/17/Ciqikou_Cross.jpg",
  "Wuhou Shrine":
    "https://upload.wikimedia.org/wikipedia/commons/thumb/1/1d/Pavilion_-_Wuhou_Shrine_-_Chengdu%2C_China_-_DSC05444.jpg/960px-Pavilion_-_Wuhou_Shrine_-_Chengdu%2C_China_-_DSC05444.jpg",
};

const bad = (s) => !s || /logo|\.svg/i.test(s);
const UA = "ChinaTripApp/1.0 (https://github.com/kborowsky25/china-trip; personal trip app)";
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function fromREST(title) {
  try {
    const enc = encodeURIComponent(title.replace(/ /g, "_"));
    const r = await fetch(`https://en.wikipedia.org/api/rest_v1/page/summary/${enc}`, {
      headers: { accept: "application/json", "User-Agent": UA },
    });
    if (!r.ok) return null;
    const j = await r.json();
    let s = j?.thumbnail?.source || j?.originalimage?.source || null;
    if (s) s = s.replace(/\/\d+px-/, "/800px-");
    return bad(s) ? null : s;
  } catch {
    return null;
  }
}

async function fromQuery(title) {
  try {
    const enc = encodeURIComponent(title);
    const r = await fetch(
      `https://en.wikipedia.org/w/api.php?action=query&format=json&redirects=1&prop=pageimages&piprop=thumbnail&pithumbsize=800&titles=${enc}`,
      { headers: { "User-Agent": UA } }
    );
    if (!r.ok) return null;
    const j = await r.json();
    const p = Object.values(j?.query?.pages ?? {})[0];
    const s = p?.thumbnail?.source ?? null;
    return bad(s) ? null : s;
  } catch {
    return null;
  }
}

async function pageImage(title) {
  if (OVERRIDE[title]) return OVERRIDE[title];
  return (await fromREST(title)) || (await fromQuery(title));
}

const out = {};
for (const t of TITLES) {
  const src = await pageImage(t);
  if (src) out[t] = src;
  console.log((src ? "✓" : "·") + " " + t);
  await sleep(220);
}
// retry pass for any that missed (transient throttling)
let missing = TITLES.filter((t) => !out[t]);
if (missing.length) {
  console.log("\nretrying", missing.length, "…");
  for (const t of missing) {
    const src = await pageImage(t);
    if (src) out[t] = src;
    console.log((src ? "✓" : "✗") + " " + t);
    await sleep(400);
  }
}
missing = TITLES.filter((t) => !out[t]);

let ts = "// AUTO-GENERATED by scripts/fetch-spot-images.mjs — Wikipedia images per attraction.\n";
ts += "export const SPOT_IMAGES: Record<string, string> = {\n";
for (const [k, v] of Object.entries(out)) ts += `  ${JSON.stringify(k)}: ${JSON.stringify(v)},\n`;
ts += "};\n\nexport function spotImageFor(title: string): string | undefined {\n  return SPOT_IMAGES[title];\n}\n";

fs.writeFileSync(path.join(process.cwd(), "mobile/lib/spotImages.ts"), ts);
console.log(`\nWrote ${Object.keys(out).length}/${TITLES.length}. Missing: ${missing.join(", ") || "none"}`);
